# 需求: 通过for循环打印 99乘法表.

"""
如果你不熟, 还是效仿昨天的思路:
    step1: 打印9行9列的 矩形.
    step2: 将上述的图形改为 正三角形.
    step3: 用 1 * 3 = 3 来替换上述的 *
    step4: 用 i 和 j 进行填充即可.
"""

for i in range(1, 10):          # 外循环控制 行数, 例如: i = 3
    for j in range(1, i + 1):   # 内循环控制 列数, 此时: j = 1, 2, 3
        print(f'{j} * {i} = {i * j}', end='\t')
    # 走到这里, 说明一行打印完毕, 记得换行.
    print()
