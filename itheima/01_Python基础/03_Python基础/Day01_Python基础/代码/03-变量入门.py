"""
变量介绍:
    概述:
        变量指的是内存中的一小块区域, 该区域值可以在一定范围内发生改变.
    变量的定义格式:
        变量名 = 变量值
    细节:
        1. =号两端 建议加上 空格, 阅读性更好.
        2. 变量名命名要符合 标识符的 规则和规范.
        3. 如果要打印某个变量的值, 直接写变量名即可, 千万不能加引号, 否则就变成: 打印字符串了.
"""

# 1. 定义变量a, 其初始化值为 100
a = 100     # 把数字100 赋值给 变量a

# 2. 打印变量a的值.
print(a)    # 100
print('a')  # a, 这里加了引号, 表示是1个字符串a, 输出结果是: a,   类似于: print('hello world!')

# 3. 修改变量a的值为 200
a = 200

# 4. 重新打印修改后的变量a的值.
print('a')  # a
print(a)    # 200
